									<h2 class="question">Why did you decide to start your own thing, versus work for someone else?</h2>
									<p>Working for myself is something I wanted to do for a long time but didn’t have the courage to do until recently. My background is in graphic design, so I had been working for several companies, like Hallmark Cards and TV Land/Nick@Nite, before moving on to teaching design courses full-time as an assistant professor. While teaching, I was also doing a lot of freelance work on the side, like art commissions, branding, and illustration. Both full-time and freelance [work] were fulfilling in very different ways. In one job I got to work with amazing colleagues and young people, and in the other I had total freedom over my creative endeavors. But then it got a little crazy.</p>
									<p>Because I was still teaching, my schedule was very intense with trying to manage both workloads. I was exhausted and finally realized doing both was not sustainable. So in 2014 I made the decision to take that leap and work for myself full-time.</p>
