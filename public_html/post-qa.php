									<h2 class="question">Can you remember when you first learned about your field of work? How did you discover what it was, and how did you know it was what you wanted to do?</h2>
									<p>Unofficially, I didn’t really know about graphic design until I saw Halle Berry playing the role of Angela in the film Boomerang. Angela was an art director and artist, and it was my first time seeing a woman of color doing that kind of work. It blew me away, even if it was fiction. But it wasn’t until I started applying for schools that I really learned about careers in graphic design, or what was called commercial art or graphic art at that time.</p>
									<p>Honestly, I was a kid who loved art, someone who loved to create but had very little knowledge about what I could do with those skills. I had never taken an art class (until my senior year in high school), had never been to art camp or had any real opportunity to develop my artistic skills. But somehow I knew I needed to work in a field that would allow me to be creative while having a steady income. And it seemed that graphic design would do just that.</p>
								</div>
								<div class="qa">
									<h2 class="question">What was the best piece of business advice you were given when you were starting off?</h2>
									<p>The best advice I received when I first started out was from a professor who taught my senior portfolio class. He told us to show our portfolio to anyone who was willing to look. Meaning, regardless of whether that person is hiring, reach out and ask if they can give you some constructive feedback about your work. After graduating, I moved to New York on a whim and took his advice to heart. I emailed, cold-called, and met with any creative director, designer, or art director who was willing to take a little time out of their day to look at my portfolio.</p>
									<p>I lugged that huge portfolio case all over Manhattan and some parts of Brooklyn every day I was off from my part-time design job. There was a huge bruise on the side of my leg from that portfolio hitting it as I walked up and down the subway stairs. It was my badge of honor from pounding the pavement.</p>
									<p>That experience was tough but great. It allowed me to practice presenting my work and make professional connections, all while learning the city. Eventually, all that portfolio lugging and dropping-off led to a full-time gig at TV Land/Nick@Nite. It took a year before that happened, but by then I had built up a ton of confidence and character (and buff arms), thanks to that awesome advice. I wasn’t afraid to introduce myself to anyone.</p>
