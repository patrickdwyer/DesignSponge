									<h2 class="title">German Pretzel Buns</h2>
									<p class="note">Makes 10 Pretzel Buns</p>
								</header>
								<h2 class="section-heading">Ingredients</h2>
								<ul class="supplies-list list-unstyled">
									<li>3¾ cups plus 1 tablespoon (500 g) flour (preferably white spelt or unbleached wheat)</li>
									<li>1 (¼-ounce / 7-g) envelope fast-acting yeast</li>
									<li>2 teaspoons fine sea salt</li>
									<li>1¼ cups (300 ml) water, lukewarm</li>
									<li>3 tablespoons (40 g) unsalted butter, melted and cooled</li>
									<li>3 tablespoons baking soda, for the alkaline solution</li>
									<li>Coarse sea salt</li>
								</ul>
								<h2 class="section-heading">Preparation</h2>
								<p><span class="step">1</span>  Combine the flour, yeast, and salt in a large bowl.</p>
								<p><span class="step">2</span>  In a medium bowl, whisk together the water and butter — the mixture should be lukewarm. Add to the flour mixture and mix with the dough hooks of an electric mixer for a few minutes or until well combined. If the dough is too sticky, add more flour. Transfer the dough to a work surface and continue kneading and punching it down with your hands for a few minutes until you have a smooth and elastic ball of dough. Place the dough back in the bowl, cover with a tea towel, and let rise in a warm place, or preferably in a 100°F (35°C) warm oven, for 60 minutes or until doubled in size.</p>
								<p><span class="step">3</span>  When the dough has doubled in size, punch it down, take it out of the bowl, and knead for about 30 seconds. Divide the dough into 10 equal (roughly 3-ounce / 85-g) portions. Dust your hands with flour, lay a portion of dough on the palm of one hand, and with the other hand forming a dome over the dough, roll it for about 10 seconds until its top is round and firm. This process creates surface tension and prevents the buns from becoming flat. Continue with the remaining dough then cover with a tea towel, and let rise in a warm place for about 20 minutes or until puffy.</p>
								<p><span class="step">4</span>  Preheat the oven to 425°F (220°C) and line 2 baking sheets with parchment paper.</p>
								<p><span class="step">5</span>  In a large pot, wide enough to fit 2 buns at once, bring 4¼ cups (1 l) of water and the baking soda to a boil. Watch the pot; the baking soda-water mixture will foam up. With a slotted ladle or spoon, slip two buns gently into the boiling water and cook for 30 seconds, turn them over, and cook for another 30 seconds. The buns don’t need to be completely covered with the solution, but mind that they don’t stick to the bottom. Transfer the buns to the lined baking sheets, score a cross on the buns, and sprinkle with coarse sea salt. Cook the remaining buns in the same manner then bake, 1 sheet at a time, for about 16 minutes or until golden brown. Enjoy warm with unsalted butter.</p>
